import time
import numpy as np

import matthewplotlib as mp


NUM_POINTS = 200

# configure screen
FPS = 10
WIDTH  = 80
HEIGHT = 25

# configure camera path
ORBIT_RADIUS = 4.5
ORBIT_HEIGHT = 2.5
ORBIT_SPEED = .75 # radians per second


def main():
    xyz = TEAPOT
    start_time = time.time()
    first_time = True
    while True:
        # sweep camera
        p = camera_pos(time.time() - start_time)
        V = view_matrix(p)

        # transform points to camera coordinate system
        xyz_cam = V @ (xyz - p).T

        # perspective projection
        xy = xyz_cam[0:2, :] / xyz_cam[2, :]
        
        # plot
        plot = mp.scatter(
            data=xy.T,
            xrange=(-1,1),
            yrange=(-5/8,5/8),
            height=HEIGHT,
            width=WIDTH,
        )
        if first_time:
            print(plot)
            first_time = False
        else:
            print(f"\x1b[{plot.height}A{plot}")

        time.sleep(1/FPS)


def camera_pos(t: float) -> np.ndarray:
    angle = ORBIT_SPEED * t
    return np.array([
        ORBIT_RADIUS * np.sin(angle),
        ORBIT_HEIGHT,
        ORBIT_RADIUS * np.cos(angle),
    ])


def view_matrix(
    eye_pos: np.ndarray,
    obj_pos: np.ndarray = np.zeros(3),
    up: np.ndarray = np.array([0,1,0]),
) -> np.ndarray:
    z = obj_pos - eye_pos
    z /= np.linalg.norm(z)
    x = np.cross(up, z)
    x /= np.linalg.norm(x)
    y = np.cross(z, x)
    return np.array([x,y,z])


